#include "libraryFun.h"

int computeSum(int num1, int num2){
    return num1 + num2;
}

int computeDifference(int num1, int num2){
    return num1 - num2;
}

int computeProduct(int num1, int num2){
    return num1 * num2;
}

float computeQuotient(int num1, int num2){
    return (float)num1 / num2;
}